'''
Створіть дві змінні first=10, second=30. Виведіть на екран результат математичної
взаємодії (+, -, *, / и тд.) для цих чисел.
'''
first = 10
second = 30
print(first + second)
print(first - second)
print(first * second)
print(first / second)
'''
Створіть змінну и по черзі запишіть в неї результат порівняння (<, > , ==, !=) чисел з завдання 1.
Виведіть на екран результат кожного порівняння.
'''
comparison = first > second
print(comparison)
comparison = first < second
print(comparison)
comparison = first == second
print(comparison)
comparison = first != second
print(comparison)

